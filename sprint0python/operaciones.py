# Defino la función de sumar
def suma(a, b):
    return a + b
# Defino la función de restar
def resta(a, b):
    return a - b
# Defino la función de multiplicar
def multiplicacion(a, b):
    return a * b
# Defino la función de division
def division(a, b):
    if b == 0:
        return "Error: División por cero"
    else:
        return a / b
